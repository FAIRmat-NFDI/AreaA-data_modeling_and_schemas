        """
        Generate the plotly figures for the `UVVisNirTransmissionResult` section.

        Returns:
            list[PlotlyFigure]: The plotly figures.
        """
        line_linear = px.line(
            x=self.results[0].wavelength,
            y=self.results[0].transmittance,
            labels={
                'x': 'Wavelength (nm)',
                'y': 'Transmission',
            },
            title='Transmission',
        )
        figure = [
            PlotlyFigure(
                label='Linear Plot',
                figure=line_linear.to_plotly_json(),
            ),
        ]
        return figure

